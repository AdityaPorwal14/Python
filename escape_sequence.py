"""backslash is used to introduce special character coding known as escape sequence
escape sequence let us embeded character in string that cannot easily be typed on a keyboard"""
# \\ escape sequence
print("male\\female")
#print("c:\new folder\text.txt")
print("c:\\new folder\\text.txt")
# learn about \ escape sequence
#print('porwal's shop')
print('porwal\'s shop')
print('python\'s program')
# learn about \ escape sequence 
#print("python"s program")
print("python\"s program")
# learn about \a escape sequence
print("hello\a hii")
# learn about \b escape sequence
print("hello\rfow")